fun main(args: Array<String>) {
    println("....Add numbers...")
    var a = readLine()!!.toInt()
    var b = readLine()!!.toInt()
    println(a+b)

    println("....Subtract numbers...")
    var c = readLine()!!.toInt()
    var d = readLine()!!.toInt()
    println(c-d)

    println("....Multiply numbers...")
    var e = readLine()!!.toInt()
    var f = readLine()!!.toInt()
    println(e*f)

    println("....Divide numbers...")
    var g = readLine()!!.toInt()
    var h = readLine()!!.toInt()
    println(g/h)
}
